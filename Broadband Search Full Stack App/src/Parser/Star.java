package Parser;

/** This is a class that allows you to initialize new Star objects. */
public class Star {

  public String id;
  public String name;
  public String x;
  public String y;
  public String z;

  public Star(String id, String name, String x, String y, String z) {
    this.id = id;
    this.name = name;
    this.x = x;
    this.y = y;
    this.z = z;
  }
}
